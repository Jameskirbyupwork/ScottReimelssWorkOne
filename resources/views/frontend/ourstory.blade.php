							<h1 class="entry-title main_title">Our Story</h1>

							<div class="entry-content">
								<p><strong>naisA Global</strong>&nbsp;was founded in April&nbsp;2013 and received its 501c3 non-profit status in November 2014 by <strong>Dr. Jamie Younghee Sheen</strong> with a vision to build the next generation of great Asian leaders. naisA Global is led by a team of accomplished professionals who have recognized the disproportion between high achieving Asian Americans, and their present lack of representation in leadership roles within large corporations, universities, and government. Often referred to as the “bamboo ceiling” or “sticky floor,” this is largely cited as a result of a combination of individual, cultural, and organizational factors impeding Asian professionals from career progress. naisA Global’s goal is to educate and empower emerging young Asian leaders across the world by providing them with mentorship opportunities, leadership training, and tools that foster and develop effective communication skills. Our programs were established in response to the critical lack of Asian leadership that exists nationally and globally.</p>
								<h4><img class="wp-image-15517 alignright" src="https://naisaglobal.org/staging/wp-content/uploads/2017/08/Photo-for-our-story-155x77.jpg" alt="" width="594" height="296" srcset="https://naisaglobal.org/staging/wp-content/uploads/2017/08/Photo-for-our-story-155x77.jpg 155w, https://naisaglobal.org/staging/wp-content/uploads/2017/08/Photo-for-our-story-600x298.jpg 600w, https://naisaglobal.org/staging/wp-content/uploads/2017/08/Photo-for-our-story-768x381.jpg 768w, https://naisaglobal.org/staging/wp-content/uploads/2017/08/Photo-for-our-story-260x129.jpg 260w" sizes="(max-width: 594px) 100vw, 594px"><strong class="headings">Addressing the Global Challenge</strong></h4>
								<p>The International Monetary Fund estimates that by 2030, Asia’s share of the world GDP will reach more than 40%. This means that businesses will need high caliber Asian leadership to drive their growth. People who possess the necessary skills, behavior and personality critical to great leadership are paramount to a thriving economy. The challenge facing this global market is a serious shortage within the Asian leadership pool.</p>
								<h4><strong class="headings">Leading in Achievement yet Lacking in Leadership</strong></h4>
								<p>Asian students consistently demonstrate outstanding academic achievement, and a high rate of Asian graduates continue to earn advanced degrees and land careers at some of the best companies around the world. In fact, 25% of Ivy League students are Asian, and Asians represent 50% of the workforce in Silicon Valley, the hub of the US Tech Industry. Despite these accomplishments, the number of Asians occupying leadership positions in America is disproportionately low, with only:</p>
								<ul>
									<li>2% of Fortune 500 CEOs</li>
									<li>2% of college presidents</li>
									<li>Less than 1% of board members</li>
									<li>Around 0.3% of corporate officers</li>
								</ul>
								<p>On a global scale, the rate of Asians in significant leadership roles is even lower.</p>
								<h4><strong class="headings">Transforming the Landscape</strong></h4>
								<p>We aim to address these challenges by providing a global platform to both aspiring and established Asian leaders from across the world and various industries, to learn, develop and contribute. Through our premier educational programs and services such as our flagship <a href="https://naisaglobal.org/staging/mentor-protege-program/">Mentor-Protégé Program</a> and other strategic initiatives, we are dedicated to not only raise awareness of the leadership talents issues facing the global Asian community, but also help young, talented Asian professionals overcome professional barriers so they can take charge of their careers within various industries, to learn, develop and contribute.</p>
								<h4 class="entry-title">Our Mission</h4>
								<p>naisA Global is a 501(c)(3) non-profit educational organization dedicated to helping talented young Asian professionals unlock their potential and become great leaders. It is the only non-profit of its kind to provide a global platform to educate, develop, inspire and positively impact careers of emerging Asian leaders around the world and across various industries – business, technology, law, medicine, arts, science, politics and non-profits.</p>
								<p><strong class="headings">Our goals:</strong></p>
								<ul>
									<li><span style="font-size: 11pt;">Educate young Asian professionals to take the lead in their careers</span></li>
									<li><span style="font-size: 11pt;"><span style="font-size: 11pt;">Prepare young Asian professionals for global leadership roles through our premier mentoring programs</span></span></li>
									<li><span style="font-size: 11pt;">Help raise an awareness of the business challenges members of the Asian community face and learn strategies for overcoming them</span></li>
									<li><span style="font-size: 11pt;">Train young Asian professionals around the world in the art of effective English communication and team building</span></li>
									<li><span style="font-size: 11pt;">Provide both established and young Asian professionals a global platform to share stories and address unique challenges associated with leadership roles</span></li>
								</ul>
